/**
 * Выбирает случайное количество симптомов (от 1 до maxCount) для указанной фазы
 */
export function getRandomSymptoms(phase, maxCount = 3) {
    const list = SYMPTOMS[phase];
    if (!list || list.length === 0) return [];
    
    // Перемешиваем массив случайным образом
    const shuffled = [...list].sort(() => 0.5 - Math.random());
    
    // Определяем, сколько именно выпадет штук (от 1 до maxCount)
    const count = Math.floor(Math.random() * maxCount) + 1;
    
    return shuffled.slice(0, count);
}